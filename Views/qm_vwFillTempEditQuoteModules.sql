                      qm.module_summary.module_name, qm.module_summary.protected, qm.quote_has_module.qty, CASE WHEN isnull(qm.quote_has_module.qty, 0) 
                      = 0 THEN 'Z' ELSE 'N' END AS qz
FROM         qm.module_summary LEFT OUTER JOIN
                      qm.quote_has_module ON qm.module_summary.module_name = qm.quote_has_module.module_name
